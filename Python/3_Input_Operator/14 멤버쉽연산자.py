### 멤버쉽 연산자 (in 연산자) ###
print('안녕' in '안녕하세요')           # 포함되어 있다.        ++ 안녕이라는 글자가 뒤에 포함되어 있습니까? 하는 느낌
print('메롱' in '안녕하세요')           # 포함되어 있지 않다.

print('안녕' not in '안녕하세요')       # 포함되어 있지 않냐 물어보는 느낌

