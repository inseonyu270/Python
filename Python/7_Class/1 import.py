### 외부 모듈의 이용 ###
# 1. 패키지
# 모듈의 상위 개념., 모듈의 집합을 의미
# 파이썬에서 기본적으로 제공하지 않더라도 외부에서 만들어진 패키지를 이용하면
# 패키지에 포함된 모듈을 사용할 수 있음.


# 2. 패키지 관리자
# 외부 모듈을 사용하기 위해서는 우선 모듈이 포함된 패키지를 추가로 설치
# pip라고 불리는 관리자를 사용해서 패키지를 추가나 삭제

# pip(PyPI, Python Package Index)에서 패키지를 검색하고 설치하는 도구
# Python 개발자가 외부 패키지를 쉽게 설치하고 관리 및 사용을 할 수 있도록 도와주는 도구임.

# 2-1. 패키지 설치
# 형식
# pip install package_name

# Ex)
# pip install numpy

# 2-2. 패키지 업그레이드
# 형식
# pip install --upgrade package_name : 이미 설치된 package_name에 해당하는 패키지를 최신 버전으로 업그레이드

# 2-3. 패키지 삭제
# 형식
# pip unstall package_name


# Ex)
# pip unstall numpy

# 2-4. 패키지 목록 확인
# pip list



# 3. 패키지 설치 (cmd 화면에서도 가능)
# pip install package


# 4. 패키지 삭제
# pip uninstall package
# pip uninstall numpy


